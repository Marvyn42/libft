#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		s;
	char	*dest;

	i = 0;
	s = ft_strlen(s1);
	if (!(dest = malloc(sizeof(*dest) * s)))
		return (NULL);
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
